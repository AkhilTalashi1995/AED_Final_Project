    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(emailText.getText().isEmpty() || !Validation.validateEmail(emailText.getText())) {

            JOptionPane.showMessageDialog(this, "Email not correct");
            return;
        }
        OneTimePassword otp = new OneTimePassword();
        String otpGenerated =  otp.getOTP(4);
        Email.sendMail(otpGenerated, emailText.getText());
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("OtpJPanel", new OneTimePasswordJPanel(userProcessContainer, otpGenerated));
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_jButton3ActionPerformed

