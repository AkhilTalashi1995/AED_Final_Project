    private String mName;
    private Role mRole;
    private Organization mOrg;
    private int selectedRowInTable;
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        mOrg = organizationDir.getOrganization(organizationEmpJComboBox.getSelectedIndex());
        
        addJButton.setEnabled(false);
        selectedRowInTable = organizationJTable.getSelectedRow();
        
        JOptionPane.showMessageDialog(this,selectedRowInTable);
        organizationEmpJComboBox.setSelectedItem(organizationJComboBox.getSelectedItem());
        
        mId = mOrg.getEmployeeDirectory().getEmployeeList().get(selectedRowInTable).getId();
        mName = mOrg.getEmployeeDirectory().getEmployeeList().get(selectedRowInTable).getName();
        mRole = mOrg.getEmployeeDirectory().getEmployeeList().get(selectedRowInTable).getRole();

        nameJTextField.setText(mName);
         
    }//GEN-LAST:event_jButton2ActionPerformed

