    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        mOrg = (Organization) organizationEmpJComboBox.getSelectedItem();
        mName = nameJTextField.getText();
        
        Employee e = new Employee();
        e.setId(mId);
        e.setName(mName);
        e.setRole(mRole);
        
        mOrg.getEmployeeDirectory().getEmployeeList().remove(selectedRowInTable);
        mOrg.getEmployeeDirectory().getEmployeeList().add(selectedRowInTable, e);
        
        JOptionPane.showMessageDialog(this,"Updated");
        addJButton.setEnabled(true);
        populateTable(mOrg);  
        nameJTextField.setText(" ");
    }//GEN-LAST:event_jButton3ActionPerformed


